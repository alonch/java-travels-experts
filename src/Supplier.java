public class Supplier {

	private int id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int i) {
		this.id = i;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
	public static void main(String[] args) {
		System.out.println("hello world");
	}

}
